#!/usr/bin/env node
/**
 * T214+T223 — поиск ключей в en.json/pl.json которые содержат кириллицу
 * (значит русский placeholder из T200 sync). Записывает:
 *   1. docs/i18n-todo.md — список ключей с русским текстом + локальный fallback
 *   2. Удаляет из en/pl.json — пусть i18next делает fallback на ru (через
 *      i18n config fallbackLng: 'ru'). Это правильнее чем показывать
 *      русский текст под видом английского.
 */

import { readFileSync, writeFileSync } from 'node:fs'
import { resolve } from 'node:path'

const REPO_ROOT = resolve(import.meta.dirname, '..')
const FILES = {
  en: `${REPO_ROOT}/apps/web/src/i18n/locales/en.json`,
  pl: `${REPO_ROOT}/apps/web/src/i18n/locales/pl.json`,
}

const CYRILLIC_RE = /[А-Яа-яЁё]/

function walk(obj, path, callback) {
  for (const key of Object.keys(obj)) {
    const value = obj[key]
    const fullPath = path ? `${path}.${key}` : key
    if (typeof value === 'string') {
      callback(fullPath, value, obj, key)
    } else if (value && typeof value === 'object') {
      walk(value, fullPath, callback)
    }
  }
}

const todos = { en: [], pl: [] }

for (const [locale, file] of Object.entries(FILES)) {
  const json = JSON.parse(readFileSync(file, 'utf8'))
  walk(json, '', (path, value, parent, key) => {
    if (CYRILLIC_RE.test(value)) {
      todos[locale].push({ path, value })
      delete parent[key]
    }
  })
  writeFileSync(file, JSON.stringify(json, null, 2) + '\n')
  console.log(`${locale}: removed ${todos[locale].length} keys with Cyrillic`)
}

// Запись i18n-todo.md
const lines = [
  '# i18n TODO — pending переводы',
  '',
  '> Auto-generated by `scripts/fix-russian-in-en-pl.mjs`.',
  '> Эти ключи были автоматически добавлены в EN/PL с русским текстом',
  '> (T200 sync default-values). Удалены отсюда — i18next делает fallback',
  '> на ru-локаль через `fallbackLng: \'ru\'`. Когда будем поддерживать',
  '> EN/PL юзеров профессионально — нужно перевести и добавить обратно.',
  '',
  '## EN ключи',
  '',
]
for (const { path, value } of todos.en) {
  lines.push(`- \`${path}\` — \`${value}\``)
}
lines.push('', '## PL ключи', '')
for (const { path, value } of todos.pl) {
  lines.push(`- \`${path}\` — \`${value}\``)
}

writeFileSync(`${REPO_ROOT}/docs/i18n-todo.md`, lines.join('\n') + '\n')
console.log(`\nWrote docs/i18n-todo.md with ${todos.en.length + todos.pl.length} TODOs`)
process.exit(0)
