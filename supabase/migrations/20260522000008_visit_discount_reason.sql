-- Скидка на этапе «Рассчитать визит»: обязательная причина если скидка ≠ 0.
-- Колонка nullable — null = нет скидки. Когда discount_cents > 0,
-- frontend гарантирует наполненность discount_reason; check constraint
-- не делаем чтобы не сломать импорт исторических данных.
-- =============================================================================

alter table public.visits
  add column if not exists discount_reason text;

comment on column public.visits.discount_reason is
  'Причина скидки (обязательно если discount_cents > 0). Заполняется на шаге расчёта визита.';
