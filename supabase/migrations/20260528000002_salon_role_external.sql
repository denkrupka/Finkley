-- Добавляем роль 'external' в salon_role enum.
--
-- Внешний пользователь — пресет по дефолту пустой (никаких прав).
-- Используется когда нужно дать кому-то ограниченный доступ только к
-- конкретным разделам (через salon_members.permissions точечно).
--
-- Пример: подрядчик, который должен видеть только Маркетинг → Контент,
-- или внешний бухгалтер которого приглашаем на pre-audit с view на P&L.
--
-- В RLS-функциях is_salon_admin / salon_role_of роль 'external' не
-- наследует прав — права только из permissions JSON.
-- =============================================================================

alter type salon_role add value if not exists 'external';
