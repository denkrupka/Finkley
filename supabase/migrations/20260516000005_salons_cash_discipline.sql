-- 20260516000005_salons_cash_discipline.sql
--
-- Включатель «кассового дня» на уровне салона. Если выключен — таб «Касса»
-- в Финансах не виден, и гейты на расчёт визита/расход/продажу не работают
-- (юзер может действовать как раньше, без открытия смены).
--
-- Default = false: фича opt-in. Существующие салоны не получают сюрпризов.
-- ─────────────────────────────────────────────────────────────────────────────

alter table salons
  add column if not exists cash_discipline_enabled boolean not null default false;

comment on column salons.cash_discipline_enabled is
  'Включено ли в салоне понятие «кассового дня». Если true — расчёт визита и расходы требуют открытой смены текущего юзера.';
