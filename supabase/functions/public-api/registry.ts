/**
 * public-api/registry.ts — ЕДИНЫЙ источник истины для публичного API Finkley.
 *
 * Декларативный реестр ресурсов. И сама Edge Function (index.ts), и публичная
 * страница документации (через discovery-эндпоинт GET /v1) читают этот реестр,
 * поэтому документация физически не может разойтись с реальным API.
 *
 * Колонки взяты дословно из apps/web/src/types/supabase.ts (сгенерированные
 * типы). Server-managed поля (id, salon_id, created_at, …) намеренно не входят
 * в create/update — даже если клиент их пришлёт, движок их выкинет.
 *
 * Принципы безопасности (см. decisions/032-public-api.md):
 *  - Каждый запрос жёстко ограничен салоном ключа (salonColumn / parentScope /
 *    bankScope). Клиент НЕ может задать salon_id.
 *  - Ресурсы со `salonColumn: null` скоупятся через проверенного родителя и
 *    в v1 доступны только на чтение.
 *  - RPC-аналитика — read-only; включены только функции без auth.uid()-guard
 *    (иначе вызов через service-role всегда падал бы с forbidden).
 *  - Глобальные/служебные таблицы (media_posts, api_keys, billing, secrets,
 *    audit, profiles, …) в реестр НЕ входят вовсе.
 */

export type ResourceKind = 'table' | 'rpc'

/** Маппинг query-параметра клиента на аргумент RPC. */
export type RpcArgMap = {
  /** имя query-параметра (from/to/limit/tz/horizon_days) */
  query: string
  /** имя аргумента функции (p_period_start, …) */
  rpcParam: string
  required?: boolean
  /** числовой аргумент (limit/horizon_days) */
  numeric?: boolean
}

/** Скоуп через родительскую запись (для таблиц без прямой колонки салона). */
export type ParentScope = {
  /** обязательный query-параметр с id родителя */
  param: string
  /** колонка на этой таблице для фильтра (обычно == param) */
  column: string
  /** родительская таблица для проверки принадлежности салону */
  parentTable: string
  /** колонка салона в родителе */
  parentSalonColumn: string
}

export type ResourceDef = {
  resource: string
  table: string
  kind: ResourceKind
  pk: string
  /** прямая колонка салона ('salon_id' | 'id') или null (parent/bank scope) */
  salonColumn: string | null
  softDeleteColumn: string | null
  read: string[]
  create: string[]
  update: string[]
  allowDelete: boolean
  /** колонки, по которым разрешён точный фильтр ?col=value */
  filters: string[]
  /** колонка для диапазона ?from=&to= (timestamp/date) или null */
  dateColumn: string | null
  defaultOrder: { column: string; ascending: boolean } | null
  /** колонки в целых минорных единицах (копейки/центы) */
  money: string[]
  /** обязательные поля при создании */
  required: string[]
  /** для kind==='rpc' */
  rpc?: { fn: string; salonArg: string; args: RpcArgMap[] }
  /** parent-scope для таблиц без прямой колонки салона */
  parentScope?: ParentScope
  /** спец-scope для банковских таблиц */
  bankScope?: 'accounts' | 'transactions'
  description: string
}

const order = (column: string, ascending = false) => ({ column, ascending })

