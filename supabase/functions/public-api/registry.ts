export function findResource(slug: string): ResourceDef | undefined {
  return RESOURCES.find((r) => r.resource === slug)
}

/**
 * Публичный каталог для discovery-эндпоинта и страницы документации.
 * Не раскрывает ничего секретного — только форму API.
 */
export function buildCatalog() {
  return {
    version: 'v1',
    money_note: 'Все суммы (*_cents) — целые минорные единицы (копейки/центы).',
    auth: 'Authorization: Bearer <fnk_live_…> или заголовок x-api-key.',
    scopes: ['read', 'write'],
    pagination: { default_limit: 50, max_limit: 200, params: ['limit', 'offset'] },
    resources: RESOURCES.map((r) => {
      const methods: string[] = ['GET']
      if (r.kind === 'table') {
        if (r.create.length) methods.push('POST')
        if (r.update.length) methods.push('PATCH')
        if (r.allowDelete) methods.push('DELETE')
      }
      return {
        resource: r.resource,
        kind: r.kind,
        path: `/v1/${r.resource}`,
        methods,
        description: r.description,
        readable_fields: r.read,
        writable_fields_create: r.create,
        writable_fields_update: r.update,
        required_on_create: r.required,
        filters: r.filters,
        date_range: r.dateColumn ? ['from', 'to'] : [],
        money_fields: r.money,
        rpc_params: r.rpc ? r.rpc.args.map((a) => ({ name: a.query, required: !!a.required })) : [],
        parent_filter: r.parentScope ? r.parentScope.param : r.bankScope ? null : null,
      }
    }),
  }
}
